#include "define.h"

/**********************************************************
* delay @12MHz about delay 26us
**********************************************************/
void delay(void)
{
    Uint8 i;

    for(i = 0; i < 10; i ++) _nop_();
}

/**********************************************************
* Delay10us @12MHz
**********************************************************/
void Delay10us(Uint8 n)
{
    Uint8 i;
	
	while(n--)
    	for(i = 0; i < 3; i ++) _nop_();
}

/**********************************************************
* Delay1ms @12MHz
**********************************************************/
void Delay1ms(Uint8 n)
{
    Uint8 i;

    while(n--)
        for(i=0;i<250;i++);
}

/**********************************************************
* Delay10ms @12MHz
**********************************************************/
void Delay10ms(Uint8 n)
{
    Uint8 i,j;
    while(n--)
    {
        for(i=0;i<22;i++)
            for(j=0;j<250;j++);
    }
}

/**********************************************************
* Delay100us @12MHz
**********************************************************/
void Delay100us(Uint8 n)
{
    Uint8 i;
    while(n--)
        for(i=0;i<23;i++);
}